class EdgeReversalSolver:
    def __init__(self, edges, n):
        self.n = n
        self.edges = edges
        self.graph = {}
        self.build_graph()

    def build_graph(self):
        # Build adjacency list with weights
        for edge in self.edges:
            start = edge[0]
            end = edge[1]

            # original direction has weight 0
            if start in self.graph:
                self.graph[start].append((end, 0))
            else:
                self.graph[start] = [(end, 0)]

            # reversed direction has weight 1
            if end in self.graph:
                self.graph[end].append((start, 1))
            else:
                self.graph[end] = [(start, 1)]

    def minimumEdgeReversal(self, src, dst):
        # distance array
        dist = []
        for i in range(self.n):
            dist.append(10**9)
        dist[src] = 0

        # queue using a list
        queue = []
        queue.append(src)

        while len(queue) > 0:
            node = queue[0]       # front of the queue
            queue = queue[1:]     # remove front

            if node in self.graph:
                neighbors = self.graph[node]
                i = 0
                while i < len(neighbors):
                    next_node = neighbors[i][0]
                    weight = neighbors[i][1]

                    if dist[node] + weight < dist[next_node]:
                        dist[next_node] = dist[node] + weight
                        if weight == 0:
                            # add to front manually
                            queue = [next_node] + queue
                        else:
                            # add to back
                            queue = queue + [next_node]
                    i += 1

        if dist[dst] == 10**9:
            return -1
        else:
            return dist[dst]


